package programms;

public class Looping {

	public static void main(String[] args) {
		
		// 1. While loop - If there is no increment while loop should execute infinite
		int i=1;
		while(i<=10) {
			System.out.println(i);
			i++;
		}
		
		System.out.println("*** For Loop ***");
		// 2. For loop
		
		for(int j=1;j<=10;j++) {
			System.out.println(j);
		}

	}

}
